var mains = document.getElementById("mains");
var mogap = document.getElementById("mogap");
mains.onclick = function() {
  var cunt = document.getElementById("cunt").innerHTML;
  var cunt_int = parseInt(cunt)
  cunt = cunt_int-1
}
mogap.onclick = function() {
  var cunt = document.getElementById("cunt").innerHTML;
  var cunt_int = parseInt(cunt)
  cunt = cunt_int+1
}